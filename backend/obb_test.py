from ultralytics import YOLO


# ---------------------------------------------------
# LOAD OBB MODEL
# ---------------------------------------------------

model = YOLO("yolov8n-obb.pt")


# ---------------------------------------------------
# IMAGE PATH
# ---------------------------------------------------

image_path = "data/test_image.jpg"


# ---------------------------------------------------
# RUN OBB DETECTION
# ---------------------------------------------------

results = model(image_path)


# ---------------------------------------------------
# PRINT RESULTS
# ---------------------------------------------------

print("\nOBB Detection Results:\n")

result = results[0]

print(result)


# ---------------------------------------------------
# PRINT OBB DATA
# ---------------------------------------------------

print("\nOriented Bounding Boxes:\n")

print(result.obb)